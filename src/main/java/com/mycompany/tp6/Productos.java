private void llenarTabla(){
    String nombre = jNombre.getText();
    String categoria = jCategoria.getSelectedItem().toString();
    double precio = Double.parseDouble(jPrecio.getText());
    Productosjava a1 = new Productosjava();
    listaProductos.add(a1);
    
}

public void cabeceraTabla(){
    String[] columnas = {"Nombre", "Categoria", "Precio"};
    productos.setColumnIdentifiers(columnas);
    jTable.setModel(productos);
}

private void setDatos(){
    Object[] datos = new Object[productos.getColumnCount()];
    int i=1;
    for (Productosjava prod : listaProductos){
        datos[0] = prod.getNombre();
        datos[1] = prod.getCategoria();
        datos[2] = prod.getPrecio();
        i++;
        productos.addRow(datos);
    }
    jTable.setModel(productos);
}
